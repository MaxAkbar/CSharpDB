using System.Diagnostics;
using CSharpDB.Benchmarks.Infrastructure;
using CSharpDB.Engine;
using CSharpDB.Primitives;
using CSharpDB.Storage.Checkpointing;
using CSharpDB.Storage.Paging;
using CSharpDB.Storage.Wal;

namespace CSharpDB.Benchmarks.Macro;

/// <summary>
/// Focused insert-side comparison for the remaining phase-4 question:
/// shared auto-commit hot inserts versus overlapping explicit WriteTransaction commits.
/// </summary>
public static class InsertFanInDiagnosticsBenchmark
{
    private const int ExplicitIdWriterRangeSpan = 1_000_000;
    private static int _nextExplicitId;
    private static int[] _nextDisjointExplicitIds = [];

    private static readonly InsertFanInScenario[] s_scenarios =
    [
        new("AutoCommit_ExplicitId_W4_Batch250us", CommitMode.AutoCommit, InsertIdMode.ExplicitId, 4, TimeSpan.FromMilliseconds(0.25)),
        new("AutoCommit_ExplicitId_W8_Batch250us", CommitMode.AutoCommit, InsertIdMode.ExplicitId, 8, TimeSpan.FromMilliseconds(0.25)),
        new(
            "AutoCommit_ExplicitIdDisjoint_W4_Batch250us",
            CommitMode.AutoCommit,
            InsertIdMode.ExplicitId,
            4,
            TimeSpan.FromMilliseconds(0.25),
            InsertKeyPattern.DisjointWriterRange),
        new(
            "AutoCommit_ExplicitIdDisjoint_W8_Batch250us",
            CommitMode.AutoCommit,
            InsertIdMode.ExplicitId,
            8,
            TimeSpan.FromMilliseconds(0.25),
            InsertKeyPattern.DisjointWriterRange),
        new(
            "AutoCommitConcurrent_ExplicitIdDisjoint_W4_Batch250us",
            CommitMode.AutoCommit,
            InsertIdMode.ExplicitId,
            4,
            TimeSpan.FromMilliseconds(0.25),
            InsertKeyPattern.DisjointWriterRange,
            ImplicitInsertExecutionMode.ConcurrentWriteTransactions),
        new(
            "AutoCommitConcurrent_ExplicitIdDisjoint_W8_Batch250us",
            CommitMode.AutoCommit,
            InsertIdMode.ExplicitId,
            8,
            TimeSpan.FromMilliseconds(0.25),
            InsertKeyPattern.DisjointWriterRange,
            ImplicitInsertExecutionMode.ConcurrentWriteTransactions),
        new("AutoCommit_AutoId_W4_Batch250us", CommitMode.AutoCommit, InsertIdMode.AutoGeneratedId, 4, TimeSpan.FromMilliseconds(0.25)),
        new("AutoCommit_AutoId_W8_Batch250us", CommitMode.AutoCommit, InsertIdMode.AutoGeneratedId, 8, TimeSpan.FromMilliseconds(0.25)),
        new("ExplicitTx_ExplicitId_W4_Batch250us", CommitMode.ExplicitWriteTransaction, InsertIdMode.ExplicitId, 4, TimeSpan.FromMilliseconds(0.25)),
        new("ExplicitTx_ExplicitId_W8_Batch250us", CommitMode.ExplicitWriteTransaction, InsertIdMode.ExplicitId, 8, TimeSpan.FromMilliseconds(0.25)),
        new(
            "ExplicitTx_ExplicitIdDisjoint_W4_Batch250us",
            CommitMode.ExplicitWriteTransaction,
            InsertIdMode.ExplicitId,
            4,
            TimeSpan.FromMilliseconds(0.25),
            InsertKeyPattern.DisjointWriterRange),
        new(
            "ExplicitTx_ExplicitIdDisjoint_W8_Batch250us",
            CommitMode.ExplicitWriteTransaction,
            InsertIdMode.ExplicitId,
            8,
            TimeSpan.FromMilliseconds(0.25),
            InsertKeyPattern.DisjointWriterRange),
        new("ExplicitTx_AutoId_W4_Batch250us", CommitMode.ExplicitWriteTransaction, InsertIdMode.AutoGeneratedId, 4, TimeSpan.FromMilliseconds(0.25)),
        new("ExplicitTx_AutoId_W8_Batch250us", CommitMode.ExplicitWriteTransaction, InsertIdMode.AutoGeneratedId, 8, TimeSpan.FromMilliseconds(0.25)),
    ];

    private static readonly WriteTransactionOptions s_retryOptions = new()
    {
        MaxRetries = 20,
    };

    public static async Task<List<BenchmarkResult>> RunAsync()
    {
        var results = new List<BenchmarkResult>(s_scenarios.Length);

        foreach (InsertFanInScenario scenario in s_scenarios)
            results.Add(await RunScenarioAsync(scenario));

        return results;
    }

    public static Task<BenchmarkResult> RunNamedScenarioAsync(string scenarioName)
    {
        ArgumentException.ThrowIfNullOrWhiteSpace(scenarioName);

        InsertFanInScenario? scenario = s_scenarios.FirstOrDefault(
            scenario => scenario.Name.Equals(scenarioName, StringComparison.OrdinalIgnoreCase));
        if (scenario is null)
        {
            throw new ArgumentException(
                $"Unknown insert fan-in scenario '{scenarioName}'.",
                nameof(scenarioName));
        }

        return RunScenarioAsync(scenario);
    }

    private static async Task<BenchmarkResult> RunScenarioAsync(InsertFanInScenario scenario)
    {
        _nextExplicitId = 0;
        _nextDisjointExplicitIds = Enumerable.Range(0, scenario.WriterCount)
            .Select(writerIndex => writerIndex * ExplicitIdWriterRangeSpan)
            .ToArray();
        var options = new DatabaseOptions
        {
            ImplicitInsertExecutionMode = scenario.ImplicitInsertExecutionMode,
        }.ConfigureStorageEngine(builder =>
        {
            builder.UsePagerOptions(new PagerOptions
            {
                CheckpointPolicy = new FrameCountCheckpointPolicy(4096),
                AutoCheckpointExecutionMode = AutoCheckpointExecutionMode.Background,
                AutoCheckpointMaxPagesPerStep = 256,
            });
            builder.UseDurableGroupCommit(scenario.BatchWindow);
        });

        await using var bench = await BenchmarkDatabase.CreateAsync(options: options);
        await bench.ReopenAsync();

        await RunPhaseAsync(bench.Db, scenario, TimeSpan.FromSeconds(2), recordLatencies: false);

        GC.Collect();
        GC.WaitForPendingFinalizers();
        GC.Collect();

        bench.Db.ResetWalFlushDiagnostics();
        bench.Db.ResetCommitPathDiagnostics();
        RunStats stats = await RunPhaseAsync(bench.Db, scenario, TimeSpan.FromSeconds(5), recordLatencies: true);
        WalFlushDiagnosticsSnapshot walDiagnostics = bench.Db.GetWalFlushDiagnosticsSnapshot();
        CommitPathDiagnosticsSnapshot commitDiagnostics = bench.Db.GetCommitPathDiagnosticsSnapshot();

        var histogram = new LatencyHistogram();
        foreach (double latencyMs in stats.CommitLatenciesMs)
            histogram.Record(latencyMs);

        double elapsedSeconds = stats.ElapsedMs <= 0 ? 0 : stats.ElapsedMs / 1000.0;
        double flushesPerSecond = elapsedSeconds <= 0 ? 0 : walDiagnostics.FlushCount / elapsedSeconds;
        double commitsPerFlush = walDiagnostics.FlushCount == 0
            ? 0
            : (double)walDiagnostics.FlushedCommitCount / walDiagnostics.FlushCount;
        double kibPerFlush = walDiagnostics.FlushCount == 0
            ? 0
            : walDiagnostics.FlushedByteCount / (double)walDiagnostics.FlushCount / 1024.0;
        int extraAttempts = Math.Max(0, stats.AttemptCount - stats.SuccessfulCommits - stats.ExhaustedConflictCount - stats.DuplicateKeyCount - stats.TerminalFailureCount);
        string commitSummary = CommitPathDiagnosticsFormatter.BuildSummary(commitDiagnostics);

        var result = new BenchmarkResult
        {
            Name = $"InsertFanIn_{scenario.Name}_5s",
            TotalOps = stats.SuccessfulCommits,
            ElapsedMs = stats.ElapsedMs,
            P50Ms = histogram.Percentile(0.50),
            P90Ms = histogram.Percentile(0.90),
            P95Ms = histogram.Percentile(0.95),
            P99Ms = histogram.Percentile(0.99),
            P999Ms = histogram.Percentile(0.999),
            MinMs = histogram.Min,
            MaxMs = histogram.Max,
            MeanMs = histogram.Mean,
            StdDevMs = histogram.StdDev,
            ExtraInfo =
                $"mode={scenario.Mode}, implicitInsertMode={scenario.ImplicitInsertExecutionMode}, idMode={scenario.IdMode}, keyPattern={scenario.KeyPattern}, writers={scenario.WriterCount}, batchWindow={FormatBatchWindow(scenario.BatchWindow)}, retryBudget={s_retryOptions.MaxRetries}, successfulCommits={stats.SuccessfulCommits}, attempts={stats.AttemptCount}, extraAttempts={extraAttempts}, exhaustedConflicts={stats.ExhaustedConflictCount}, duplicateKeys={stats.DuplicateKeyCount}, terminalFailures={stats.TerminalFailureCount}, flushes={walDiagnostics.FlushCount}, flushesPerSec={flushesPerSecond:F1}, commitsPerFlush={commitsPerFlush:F2}, KiBPerFlush={kibPerFlush:F1}, batchWindowWaits={walDiagnostics.BatchWindowWaitCount}, {commitSummary}",
        };

        Console.WriteLine(
            $"  {result.Name}: {result.OpsPerSecond:N0} commits/sec, P50={result.P50Ms:F3}ms, P99={result.P99Ms:F3}ms, commits/flush={commitsPerFlush:F2}, maxPending={commitDiagnostics.MaxPendingCommitCount}");
        Console.WriteLine($"    {result.ExtraInfo}");
        return result;
    }

    private static async Task<RunStats> RunPhaseAsync(
        Database db,
        InsertFanInScenario scenario,
        TimeSpan duration,
        bool recordLatencies)
    {
        var startGate = new TaskCompletionSource(TaskCreationOptions.RunContinuationsAsynchronously);
        var writerTasks = new Task<WriterStats>[scenario.WriterCount];
        long durationTicks = (long)(duration.TotalSeconds * Stopwatch.Frequency);

        for (int writerIndex = 0; writerIndex < scenario.WriterCount; writerIndex++)
        {
            int localWriterIndex = writerIndex;
            writerTasks[writerIndex] = Task.Run(async () =>
            {
                var localLatencies = recordLatencies ? new List<double>(capacity: 4096) : null;
                int successfulCommits = 0;
                int attemptCount = 0;
                int exhaustedConflictCount = 0;
                int duplicateKeyCount = 0;
                int terminalFailureCount = 0;

                await startGate.Task.ConfigureAwait(false);
                long startedAt = Stopwatch.GetTimestamp();

                while (Stopwatch.GetTimestamp() - startedAt < durationTicks)
                {
                    string sql = BuildInsertSql(localWriterIndex, scenario);
                    int transactionAttempts = 0;
                    var sw = Stopwatch.StartNew();

                    try
                    {
                        if (scenario.Mode == CommitMode.AutoCommit)
                        {
                            attemptCount++;
                            await db.ExecuteAsync(sql, CancellationToken.None).ConfigureAwait(false);
                        }
                        else
                        {
                            await db.RunWriteTransactionAsync(
                                async (tx, innerCt) =>
                                {
                                    transactionAttempts++;
                                    await tx.ExecuteAsync(sql, innerCt).ConfigureAwait(false);
                                },
                                s_retryOptions).ConfigureAwait(false);
                            attemptCount += transactionAttempts;
                        }

                        sw.Stop();
                        successfulCommits++;
                        localLatencies?.Add(sw.Elapsed.TotalMilliseconds);
                    }
                    catch (CSharpDbConflictException)
                    {
                        sw.Stop();
                        attemptCount += transactionAttempts;
                        exhaustedConflictCount++;
                    }
                    catch (CSharpDbException ex) when (ex.Code == ErrorCode.DuplicateKey)
                    {
                        sw.Stop();
                        attemptCount += transactionAttempts;
                        duplicateKeyCount++;
                    }
                    catch (CSharpDbException ex) when (ex.Code == ErrorCode.Busy)
                    {
                        sw.Stop();
                        attemptCount += transactionAttempts;
                        terminalFailureCount++;
                    }
                    catch
                    {
                        terminalFailureCount++;
                        throw;
                    }
                }

                return new WriterStats(
                    localLatencies ?? [],
                    successfulCommits,
                    attemptCount,
                    exhaustedConflictCount,
                    duplicateKeyCount,
                    terminalFailureCount);
            });
        }

        var totalSw = Stopwatch.StartNew();
        startGate.TrySetResult();
        WriterStats[] completed = await Task.WhenAll(writerTasks);
        totalSw.Stop();

        var combinedLatencies = new List<double>(completed.Sum(static writer => writer.CommitLatenciesMs.Count));
        int successfulCommits = 0;
        int attemptCount = 0;
        int exhaustedConflictCount = 0;
        int duplicateKeyCount = 0;
        int terminalFailureCount = 0;

        foreach (WriterStats writer in completed)
        {
            combinedLatencies.AddRange(writer.CommitLatenciesMs);
            successfulCommits += writer.SuccessfulCommits;
            attemptCount += writer.AttemptCount;
            exhaustedConflictCount += writer.ExhaustedConflictCount;
            duplicateKeyCount += writer.DuplicateKeyCount;
            terminalFailureCount += writer.TerminalFailureCount;
        }

        return new RunStats(
            combinedLatencies,
            successfulCommits,
            attemptCount,
            exhaustedConflictCount,
            duplicateKeyCount,
            terminalFailureCount,
            totalSw.Elapsed.TotalMilliseconds);
    }

    private static string BuildInsertSql(int writerIndex, InsertFanInScenario scenario)
    {
        return scenario.IdMode == InsertIdMode.ExplicitId
            ? $"INSERT INTO bench (id, value, text_col, category) VALUES ({NextExplicitId(writerIndex, scenario)}, {writerIndex}, 'insert_fan_in', 'Alpha')"
            : $"INSERT INTO bench (value, text_col, category) VALUES ({writerIndex}, 'insert_fan_in', 'Alpha')";
    }

    private static int NextExplicitId(int writerIndex, InsertFanInScenario scenario)
    {
        return scenario.KeyPattern switch
        {
            InsertKeyPattern.HotRightEdge => Interlocked.Increment(ref _nextExplicitId),
            InsertKeyPattern.DisjointWriterRange => Interlocked.Increment(ref _nextDisjointExplicitIds[writerIndex]),
            _ => throw new ArgumentOutOfRangeException(nameof(scenario), scenario.KeyPattern, "Unsupported insert key pattern."),
        };
    }

    private static string FormatBatchWindow(TimeSpan batchWindow)
    {
        if (batchWindow == TimeSpan.Zero)
            return "0";

        double microseconds = batchWindow.TotalMilliseconds * 1000.0;
        if (microseconds < 1000.0)
            return $"{microseconds:F0}us";

        return $"{batchWindow.TotalMilliseconds:F3}ms";
    }

    private sealed record InsertFanInScenario(
        string Name,
        CommitMode Mode,
        InsertIdMode IdMode,
        int WriterCount,
        TimeSpan BatchWindow,
        InsertKeyPattern KeyPattern = InsertKeyPattern.HotRightEdge,
        ImplicitInsertExecutionMode ImplicitInsertExecutionMode = ImplicitInsertExecutionMode.Serialized);

    private enum CommitMode
    {
        AutoCommit,
        ExplicitWriteTransaction,
    }

    private enum InsertIdMode
    {
        ExplicitId,
        AutoGeneratedId,
    }

    private enum InsertKeyPattern
    {
        HotRightEdge,
        DisjointWriterRange,
    }

    private sealed record WriterStats(
        IReadOnlyList<double> CommitLatenciesMs,
        int SuccessfulCommits,
        int AttemptCount,
        int ExhaustedConflictCount,
        int DuplicateKeyCount,
        int TerminalFailureCount);

    private sealed record RunStats(
        IReadOnlyList<double> CommitLatenciesMs,
        int SuccessfulCommits,
        int AttemptCount,
        int ExhaustedConflictCount,
        int DuplicateKeyCount,
        int TerminalFailureCount,
        double ElapsedMs);
}
